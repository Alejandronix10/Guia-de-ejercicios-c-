#include <iostream>
using namespace std;

int main() {
    float radio = 3.0; // declaramos la variable que es float para calcular el radio 
    float pi = 3.14159; // se declara la variable pi
    float area = pi * radio * radio; // usamos la formula para calcular el area del circulo 

    cout << "area del circulo: " << area << endl; // muestra el resultado 

    return 0;
}
