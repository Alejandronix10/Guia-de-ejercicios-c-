#include <iostream>
using namespace std;

int main() {

    float celsius = 10.0; // Declaramos una variable celsius que nos de temperatura y los grados 
    float fahrenheit = (celsius * 9/5) + 32; // convertimos la temperatura en fahrenheit 

    cout << "la temperatura en fahrenheit es : " << fahrenheit << endl; // imprime la temperatura en fahrenheit

    return 0;
}
